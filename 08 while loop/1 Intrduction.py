#while loop: while loop executes set of stmts for repeated no of times or for multiple times

#All the stmts within the while loop should follow the indentation

#Syntax for defining the while loop:
'''
initialization
while(condition):
    stmt1
    stmt2
    stmt3
    stmt4
    increment/decrement
Stmts within the while loop will be executed untill the condition becomes false,once the
condition becomes False then ctrl comes out of while block and executes the other stmts.
'''
#To print the nos from 1 to 10

x=1            #initialization
while(x<=10):  #condition
    print(x)
    x=x+1      #increment
print("end")







    
