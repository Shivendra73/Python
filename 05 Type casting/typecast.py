#Typecasting: Converting one datatype to other type

#case 1: Converting a string to int

x=input("Enter value of x:")
print(x,type(x))

y=int(x)
print(y,type(y))

#case 2: converting a int to string
x=20
print(x,type(x))

y=str(x)
print(y,type(y))

#case 3: converting a int to float
x=30
print(x,type(x))

y=float(x)
print(y,type(y))

#case 4: Converting a float to int
x=4.5
print(x,type(x))

y=int(x)
print(y,type(y))

#case 5: converting a int to bool
x=1
print(x,type(x))

y=bool(x)
print(y,type(y))

#case 6: converting a bool to int
x=True
print(x,type(x))

y=int(x)
print(y,type(y))


















